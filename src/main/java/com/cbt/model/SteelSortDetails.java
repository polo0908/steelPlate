package com.cbt.model;

import java.io.Serializable;

public class SteelSortDetails implements Serializable {

	
	private Integer id;
	private String steelType; //钢铁类型
	private String steelSort; //详细种类
	private String imageAddr; //图片地址
	private String belongType; //从属类型
	
	
	
	public String getBelongType() {
		return belongType;
	}
	public void setBelongType(String belongType) {
		this.belongType = belongType;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getSteelType() {
		return steelType;
	}
	public void setSteelType(String steelType) {
		this.steelType = steelType;
	}
	public String getSteelSort() {
		return steelSort;
	}
	public void setSteelSort(String steelSort) {
		this.steelSort = steelSort;
	}
	public String getImageAddr() {
		return imageAddr;
	}
	public void setImageAddr(String imageAddr) {
		this.imageAddr = imageAddr;
	}
	
	@Override
	public String toString() {
		return "SteelSortDetails [id=" + id + ", steelType=" + steelType
				+ ", steelSort=" + steelSort + ", imageAddr=" + imageAddr
				+ ", belongType=" + belongType + "]";
	}
	
	
}
