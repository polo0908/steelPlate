package com.cal.bean;

import java.io.Serializable;

public class ProcessInfo implements Serializable {
  
	  
	  private Integer id;
	  private String level1;    //一级菜单
	  private String level2;    //二级菜单
	  private String level3;    //三级菜单
	  private Double unitPrice;  //单价
	  private String unit;    //单位
	  private Integer superficialState;   //是否表面积（0:不是 1:是）
	  private String level1Cn;  //中文名称
	  private String level2Explain;  //二级菜单说明
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getLevel1() {
		return level1;
	}
	public void setLevel1(String level1) {
		this.level1 = level1;
	}
	public String getLevel2() {
		return level2;
	}
	public void setLevel2(String level2) {
		this.level2 = level2;
	}
	public String getLevel3() {
		return level3;
	}
	public void setLevel3(String level3) {
		this.level3 = level3;
	}
	public Double getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(Double unitPrice) {
		this.unitPrice = unitPrice;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public Integer getSuperficialState() {
		return superficialState;
	}
	public void setSuperficialState(Integer superficialState) {
		this.superficialState = superficialState;
	}
	public String getLevel1Cn() {
		return level1Cn;
	}
	public void setLevel1Cn(String level1Cn) {
		this.level1Cn = level1Cn;
	}
	public String getLevel2Explain() {
		return level2Explain;
	}
	public void setLevel2Explain(String level2Explain) {
		this.level2Explain = level2Explain;
	}
	@Override
	public String toString() {
		return "Process [id=" + id + ", level1=" + level1 + ", level2="
				+ level2 + ", level3=" + level3 + ", unitPrice=" + unitPrice
				+ ", unit=" + unit + ", superficialState=" + superficialState
				+ ", level1Cn=" + level1Cn + ", level2Explain=" + level2Explain
				+ "]";
	}
	  
	  
	
	
}
