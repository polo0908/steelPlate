package com.cal.bean;

import java.io.Serializable;

public class CustomerDrawingName implements Serializable {

	
	private Integer id;
	private String emoldName;    //emold上名字
	private String originalName; //原始文件名
	private String quoteId;      //报价号
	private Integer customerId;   //客户Id
	
	
	
	public Integer getCustomerId() {
		return customerId;
	}
	public void setCustomerId(Integer customerId) {
		this.customerId = customerId;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getEmoldName() {
		return emoldName;
	}
	public void setEmoldName(String emoldName) {
		this.emoldName = emoldName;
	}
	public String getOriginalName() {
		return originalName;
	}
	public void setOriginalName(String originalName) {
		this.originalName = originalName;
	}
	public String getQuoteId() {
		return quoteId;
	}
	public void setQuoteId(String quoteId) {
		this.quoteId = quoteId;
	}
	@Override
	public String toString() {
		return "CustomerDrawingName [id=" + id + ", emoldName=" + emoldName
				+ ", originalName=" + originalName + ", quoteId=" + quoteId
				+ ", customerId=" + customerId + "]";
	}
	
	
	
}
