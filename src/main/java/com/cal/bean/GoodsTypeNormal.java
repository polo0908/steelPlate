package com.cal.bean;

import java.io.Serializable;

public class GoodsTypeNormal implements Serializable {
   
	private Integer id;
	private Integer goodsTypeInfoId;     //物品种类表ID
	private String parameterNormal;      //标准参数
	private String parameterType;        //参数类型
	
	
	public String getParameterType() {
		return parameterType;
	}
	public void setParameterType(String parameterType) {
		this.parameterType = parameterType;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getGoodsTypeInfoId() {
		return goodsTypeInfoId;
	}
	public void setGoodsTypeInfoId(Integer goodsTypeInfoId) {
		this.goodsTypeInfoId = goodsTypeInfoId;
	}
	public String getParameterNormal() {
		return parameterNormal;
	}
	public void setParameterNormal(String parameterNormal) {
		this.parameterNormal = parameterNormal;
	}
	@Override
	public String toString() {
		return "GoodsTypeNormal [id=" + id + ", goodsTypeInfoId="
				+ goodsTypeInfoId + ", parameterNormal=" + parameterNormal
				+ ", parameterType=" + parameterType + "]";
	}
	
	
	
	
}
